int sum(int num1, int num2)
{
    return (num1 + num2);
}

int subtrat(int num1, int num2)
{
    return (num1 - num2);
}

int mul(int num1, int num2)
{
    return (num1 * num2);
}

int division(int num1, int num2)
{
    return (num1 / num2);
}

int mod(int num1, int num2)
{
    return (num1 % num2);
}
